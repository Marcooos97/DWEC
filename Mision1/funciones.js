function areaTriangulo(b, a){
    return "El area del triangulo es: " + ((b*a)/2);
}

function perimetroTrianguloEquilatero(l){
    return "El perimetro del Triangulo Equilatero es: " + (3*l);
}

function perimetroTrianguloIsosceles(l1,l2){
    return "El perimetro del Triangulo Isosceles es: " + (2*l1 + l2);
}

function perimetroTrianguloEscaleno(l1,l2,l3){
    return "El perimetro del Triangulo Escaleno es: " + (l1+l2+l3);    
}

function areaCuadrado(l){
    return "El area del cuadrado es: " + (l*l);    
}

function perimetroCuadrado(l){
    return "El perimetro del cuadrado es: " + (4*l);    
}

function areaRectangulo(l1,l2){
    return "El area del rectangulo es: " + (l1*l2);    
}

function areaCirculo(r){
    return "El area del circulo es: " + (Math.PI * (r*r));    
}

function longitudCircunferencia(r){
    return "La longitud de la circuenferencia es: " + (2 * Math.PI * r);    
}

